namespace SolveCalculate.Lib;

public class Calculate
{
    public int CalculateAdd(int num1, int num2)
    {
        return num1 + num2;
    }
    public int CalculateSubt(int num1, int num2)
    {
        return num1 - num2;
    }
    public int CalculateMult(int num1, int num2)
    {
        return num1 * num2;
    }
    public double CalculateDiv(double num1, double num2)
    {
        return num1 / num2;
    }
}
